public class Destination {
    public int id;
    public String name;
    public String country;
    public String description;
    public String imagePath;

    public Destination(int id, String n, String c, String d, String img) {
        this.id = id;
        this.name = n;
        this.country = c;
        this.description = d;
        this.imagePath = img;
    }
}
